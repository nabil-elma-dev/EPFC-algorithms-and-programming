
package pkg01_assignations;

public class Exercice_01f {
    
    /*
    Affiche 3 5 et à la ligne suivante 5 3
    Le programme ne change pas les variables mais les affiche dans 
    l'ordre inversé
    */

    public static void main(String[] args) {
        int a = 3, b = 5;
        System.out.print(a);
        System.out.print(" ");
        System.out.println(b);
        
        System.out.print(b);
        System.out.print(" ");
        System.out.println(a);
    }

}

